/**
 * 根据ID获取预设风格
 */
export function getPresetStyle(id: string): WritingStylePreset | undefined {
  return PRESET_WRITING_STYLES.find(s => s.id === id);
}

/**
 * 根据分类获取风格
 */
export function getStylesByCategory(category: string): WritingStylePreset[] {
  return PRESET_WRITING_STYLES.filter(s => s.category === category);
}

/**
 * 获取所有分类
 */
export function getAllCategories(): string[] {
  return Array.from(new Set(PRESET_WRITING_STYLES.map(s => s.category)));
}
